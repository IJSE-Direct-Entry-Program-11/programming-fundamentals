public class Demo4 {
    
    public static void main(String[] args) {
        
        /* Data Types & Literals */

        // floating point numbers

        // decimal
        System.out.println(10.);
        System.out.println(.25);
        System.out.println(10.25);
        System.out.println(10.25e2F);
        System.out.println(10.25E2f);

        // hexadecimal
        System.out.println(0x1.p1F);
        System.out.println(0x11.p2);
        System.out.println(0xA.1p2);
        System.out.println(0XA.Fp2f);
    }
}
